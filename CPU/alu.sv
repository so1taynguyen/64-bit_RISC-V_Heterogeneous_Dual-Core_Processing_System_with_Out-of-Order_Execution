module alu (
    input  [63:0] operand_a,
    input  [63:0] operand_b,
    input  [3:0]  alu_op,
    input         W_suffix,
    output logic [63:0] alu_data
);
    timeunit 1ns; timeprecision 1ps;
    
    localparam ADD  = 4'h0;
    localparam SUB  = 4'h1;
    localparam SLT  = 4'h2;
    localparam SLTU = 4'h3;
    localparam XOR  = 4'h4;
    localparam OR   = 4'h5;
    localparam AND  = 4'h6;
    localparam SLL  = 4'h7;
    localparam SRL  = 4'h8;
    localparam SRA  = 4'h9;

    wire [64:0] ua, ub, uc;
    wire [63:0] c, s;
    wire [5:0] shift_amount;
    logic [63:0] temp_result;
    
    assign ua = {1'b0,operand_a};
    assign ub = {1'b0,operand_b};
    assign uc =        ua + ~       ub + 65'h1;
    assign  c = operand_a + ~operand_b + 64'h1;
    assign shift_amount = (W_suffix) ? ({1'b0, operand_b[4:0]}) : (operand_b[5:0]);
    assign alu_data = temp_result;

    arith_right_shift ars (
        .din(operand_a),
        .amount(shift_amount),
        .dout(s)
    );

    always_comb begin
        case (alu_op)
            ADD: begin 
                temp_result = operand_a + operand_b;
            end
            SUB: begin
                temp_result = operand_a + ~operand_b + 64'h1;
            end
            SLT: begin
                temp_result = (c[63])  ? 64'h1 : 64'h0;
            end
            SLTU: begin
                temp_result = (uc[64]) ? 64'h1 : 64'h0;
            end
            XOR: begin
                temp_result = operand_a ^ operand_b;
            end
            OR:  begin
                temp_result = operand_a | operand_b;
            end
            AND: begin
                temp_result = operand_a & operand_b;
            end
            SLL: begin
                temp_result = operand_a << shift_amount;
            end
            SRL: begin
                temp_result = operand_a >> shift_amount;
            end
            SRA: begin
                temp_result = s;
            end
            default: begin
                temp_result = 64'h0;
            end 
        endcase

        if (W_suffix) begin
            temp_result = {{32{temp_result[31]}}, temp_result[31:0]};
        end
        else begin
            temp_result = temp_result;
        end
    end

endmodule

